    private void refreshPartNameBox() {
        partNameBox.removeAllItems();
        
        ArrayList<String> parts = gui.databaseHandler.getNameArray("SELECT part_name FROM part", "part_name");
        
        Collections.sort(parts);
        
        partNameBox.addItem(null);
        parts.forEach((u) -> {
            partNameBox.addItem(u);
        });
    }
