    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // take in field details & edit user's info in database
        Object selected = partNameBox.getSelectedItem();
        int selectedIndex = partNameBox.getSelectedIndex();
        
        if (selected != null){
            String part_name = selected.toString();
            String description = descriptionField.getText();
            try {
                float price = Float.parseFloat(priceField.getText());
                try {
                    
                    int amount = Integer.parseInt(stockAmountField.getText());
                    int threshold = Integer.parseInt(thresholdField.getText());

                    String sql =  "UPDATE part"
                                + "SET description =?,"
                                + "SET price =?,"
                                + "SET stock_amount =?,"
                                + "SET threshold =?,"
                                + "WHERE part_name=?;";

                    ArrayList<ValueObject> values = new ArrayList<>();

                    values.add(new ValueObject("String", description));
                    values.add(new ValueObject("float", price));
                    values.add(new ValueObject("int", amount));
                    values.add(new ValueObject("int", threshold));

                    values.add(new ValueObject("String", part_name));

                    gui.databaseHandler.executeArrayInsert(sql, values);

                    refreshPartNameBox();
                    partNameBox.setSelectedIndex(selectedIndex);
                    editButton.setText("Saved");
                } catch (NumberFormatException e){
                    gui.throwErrorForm("Amount & Threshold must be positive whole numbers.");
                }
            } catch (NumberFormatException e){
                gui.throwErrorForm("Price must be a number.");
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        //remove user from database
        Object selected = partNameBox.getSelectedItem();
        editButton.setText("Save Changes");
        
        if (selected != null){
            gui.databaseHandler.executeStatement("DELETE FROM part WHERE part_name='" + selected.toString() + "'");
            refreshPartNameBox();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void partNameBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_partNameBoxActionPerformed
        //on selecting userID, populate fields
        Object selected = partNameBox.getSelectedItem();
        editButton.setText("Save Changes");
        
        if (selected != null){
            String part_name = selected.toString();
            String description = gui.databaseHandler.executeStringQuery("SELECT description FROM part WHERE part_name=?", part_name, "description");
            float price = gui.databaseHandler.executeFloatQuery("SELECT price FROM part WHERE part_name=?", part_name, "price");
            int amount = gui.databaseHandler.executeIntQuery("SELECT stock_amount FROM part WHERE part_name=?", part_name, "stock_amount");
            int threshold = gui.databaseHandler.executeIntQuery("SELECT threshold FROM part WHERE part_name=?", part_name, "threshold");
            
            descriptionField.setText(description);
            priceField.setText(price + "");
            stockAmountField.setText(amount + "");
            thresholdField.setText(threshold  + "");
            
        } else {
            //UserIdBox.setSelectedIndex(0);
            descriptionField.setText("");
            priceField.setText("");
            stockAmountField.setText("0");
            thresholdField.setText("");
        }
    }//GEN-LAST:event_partNameBoxActionPerformed

